/**
 *  * reset_to_98 - Resets the value of an integer to 98.
 *
 * @n: A pointer to an integer that will be reset to 98.
 *
 * Description: This function takes a pointer to an integer as its parameter
 * and updates the value it points to with the value 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
